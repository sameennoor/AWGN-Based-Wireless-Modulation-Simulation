# ============================================================
#  AWGN FUNCTION
# ============================================================
def add_awgn(signal, snr_db, bits_per_symbol):
    snr_linear = 10 ** (snr_db / 10)
    noise_power = 1 / (2 * bits_per_symbol * snr_linear)
    noise = np.sqrt(noise_power) * np.random.randn(*signal.shape)
    return signal + noise

# ============================================================
#  BPSK
# ============================================================
def simulate_bpsk(snr_db):
    bits = np.random.randint(0, 2, NUM_BITS)
    signal = 2 * bits - 1

    ber_list = []

    for snr in snr_db:
        received = add_awgn(signal.astype(float), snr, 1)
        decided = (received > 0).astype(int)

        ber = np.mean(decided != bits)
        ber_list.append(max(ber, 1e-6))

    return np.array(ber_list)

def theoretical_bpsk_ber(snr_db):
    snr_linear = 10 ** (np.array(snr_db) / 10)
    return 0.5 * erfc(np.sqrt(snr_linear))

# ============================================================
#  QPSK
# ============================================================
def simulate_qpsk(snr_db):

    bits = np.random.randint(0, 2, NUM_BITS)

    if len(bits) % 2 != 0:
        bits = bits[:-1]

    I_bits = bits[0::2]
    Q_bits = bits[1::2]

    I_signal = (2 * I_bits - 1) / np.sqrt(2)
    Q_signal = (2 * Q_bits - 1) / np.sqrt(2)

    ber_list = []

    for snr in snr_db:

        I_rx = add_awgn(I_signal, snr, 2)
        Q_rx = add_awgn(Q_signal, snr, 2)

        I_dec = (I_rx > 0).astype(int)
        Q_dec = (Q_rx > 0).astype(int)

        errors = np.sum(I_dec != I_bits) + np.sum(Q_dec != Q_bits)

        ber = errors / len(bits)

        ber_list.append(max(ber, 1e-6))

    return np.array(ber_list)

def theoretical_qpsk_ber(snr_db):

    snr_linear = 10 ** (np.array(snr_db) / 10)

    return 0.5 * erfc(np.sqrt(snr_linear))

# ============================================================
#  QAM
# ============================================================
def simulate_qam(snr_db, M):

    bits_per_sym = int(np.log2(M))

    half = int(np.sqrt(M))

    levels = np.arange(-(half - 1), half, 2)

    bits_per_dim = bits_per_sym // 2

    total_syms = NUM_BITS // bits_per_sym

    total_bits = total_syms * bits_per_sym

    bits = np.random.randint(0, 2, total_bits)

    bits_matrix = bits.reshape(total_syms, bits_per_sym)

    def bits_to_level(b_block):

        idx = b_block.dot(
            1 << np.arange(b_block.shape[1] - 1, -1, -1)
        )

        return levels[idx]

    I_sym_raw = bits_to_level(
        bits_matrix[:, :bits_per_dim]
    ).astype(float)

    Q_sym_raw = bits_to_level(
        bits_matrix[:, bits_per_dim:]
    ).astype(float)

    avg_power = np.mean(I_sym_raw**2 + Q_sym_raw**2)

    def decide(rx):

        return np.argmin(
            np.abs(rx[:, None] - levels[None, :]),
            axis=1
        )

    I_idx_tx = decide(I_sym_raw)
    Q_idx_tx = decide(Q_sym_raw)

    def gray_errors(tx, rx):

        tx_g = tx ^ (tx >> 1)
        rx_g = rx ^ (rx >> 1)

        return sum(
            bin(a ^ b).count('1')
            for a, b in zip(tx_g, rx_g)
        )

    ber_list = []

    for snr in snr_db:

        snr_linear = 10 ** (snr / 10)

        noise_std = np.sqrt(
            avg_power / (2 * bits_per_sym * snr_linear)
        )

        I_rx = I_sym_raw + noise_std * np.random.randn(total_syms)

        Q_rx = Q_sym_raw + noise_std * np.random.randn(total_syms)

        I_idx_rx = decide(I_rx)
        Q_idx_rx = decide(Q_rx)

        errors = (
            gray_errors(I_idx_tx, I_idx_rx)
            +
            gray_errors(Q_idx_tx, Q_idx_rx)
        )

        ber = errors / total_bits

        ber_list.append(max(ber, 1e-6))

    return np.array(ber_list)

def theoretical_qam_ber(snr_db, M):

    snr_linear = 10 ** (np.array(snr_db) / 10)

    k = np.log2(M)

    return (
        (4 / k)
        *
        (1 - 1 / np.sqrt(M))
        *
        0.5
        *
        erfc(
            np.sqrt(
                1.5 * k * snr_linear / (M - 1)
            )
        )
    )

