#  PERFORMANCE METRICS
# ============================================================
def calculate_performance_metrics(
    ber_curves,
    modulations,
    bits_per_symbol
):

    def find_snr_for_ber(
        snr_points,
        ber_points,
        target=1e-3
    ):

        idx = np.where(ber_points <= target)[0]

        if len(idx) > 0:
            return snr_points[idx[0]]

        return 20.0

    metrics = {}

    for mod, ber_curve, bps in zip(
        modulations,
        ber_curves,
        bits_per_symbol
    ):

        snr_sensitivity = find_snr_for_ber(
            SNR_DB,
            ber_curve
        )

        idx_10db = np.where(SNR_DB == 10)[0][0]

        ber_at_10db = ber_curve[idx_10db]

        throughput_factor = bps * (1 - ber_at_10db)

        metrics[mod] = {

            'SNR_Sensitivity_dB':
                float(snr_sensitivity),

            'BER_at_10dB':
                float(ber_at_10db),

            'SpecEff_x_Reliability':
                float(throughput_factor)
        }

    return metrics

